namespace BlockEngine.Drawing
{
    /// <summary>
    /// Used to define the location of a block within a shape.
    /// </summary>
    internal struct Location
    {
        /// <summary>
        /// Gets or sets the x.
        /// </summary>
        /// <value>
        /// The x.
        /// </value>
        public int X;

        /// <summary>
        /// Gets or sets the y.
        /// </summary>
        /// <value>
        /// The y.
        /// </value>
        public int Y;
    }
}